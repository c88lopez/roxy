                                </p><p>
                                    No es que sepa mucho pero después de meditarlo un poco creo que la solución es el NO
                                    OLVIDAR; no hablo de recordar, si no, de no olvidar. No hablo de quedarse atado A, sino, de
                                    llevar un latido vivo adentro, que de vez en vez, nos baje a tierra.
                                </p><p>
                                    Cuanto más me apabullo, más perspectiva busco. Y en ese espacio, impasse, limbo emocional,
                                    “pido gancho”. Y miro para adentro.<br />
                                    Me doy cuenta de lo importante, importantísimo que es, no olvidar:
                                </p><p>
                                    No olvidarse de uno; de la esencia, de lo puro, de lo chiquito. De nuestra inocencia. De lo que
                                    nos hace reír: de los payasos, los juegos, los bailes. De todo lo que vive adentro, y genera
                                    endorfina. No olvidar quién soy más allá del afuera.
                                </p><p>
                                    No olvidarse de los otros; en el medio de la crisis, en general, la única sombra que vemos es la
                                    nuestra. Y puede que, tal vez esa sombra, sea de la persona que nos está “alzando” sin saberlo.<br />
                                    La que nos tiene entre sus brazos. En la crisis no vemos mas nada que nuestra nariz. Por eso
                                    la perspectiva es sana. Porque siempre hay otros tendiendo manos que no podemos ver. Y
                                    porque los demás nos hacen mejores. Nos dan alas cuando las nuestras están rotas. Y también
                                    nos redimen. Nos empujan a superarlos o a mejorar a su lado: nos obligan a esforzarnos. No
                                    olvidar quién soy entre medio de ellos.
                                </p><p>
                                    Pero sobre todo, creo que el lazo que sostiene, es no olvidar aquello que estaba incluso, desde
